public class CoWorker {

    String name;
    int id;
    int salary;

    public CoWorker(String n,int i,int s)
    {
        name = n;
        id = i;
        salary = s;

    }

    public String getName(){
        return name;
    }
    public int getId(){
        return id;
    }
    public int getSalary(){
        return salary;
    }

}
